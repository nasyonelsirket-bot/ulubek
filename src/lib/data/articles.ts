export function getAllArticles(): Article[] {
  return [...articles].sort(
    (a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
  );
}

export function getArticleBySlug(slug: string): Article | undefined {
  return articles.find((a) => a.slug === slug);
}

export function getFeaturedArticles(): Article[] {
  return getAllArticles().filter((a) => a.featured);
}

export function getBreakingNews(): Article[] {
  return getAllArticles().filter((a) => a.breaking);
}

export function getArticlesByCategory(categoryId: string): Article[] {
  return getAllArticles().filter((a) => a.categoryId === categoryId);
}

export function getRelatedArticles(article: Article, limit = 4): Article[] {
  return getAllArticles()
    .filter((a) => a.categoryId === article.categoryId && a.id !== article.id)
    .slice(0, limit);
}

export function searchArticles(query: string): Article[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return getAllArticles().filter(
    (a) =>
      a.title.toLowerCase().includes(q) ||
      a.excerpt.toLowerCase().includes(q) ||
      a.tags.some((t) => t.toLowerCase().includes(q))
  );
}

export function getLatestArticles(limit = 10): Article[] {
  return getAllArticles().slice(0, limit);
}

export function getPopularArticles(limit = 5): Article[] {
  return getAllArticles()
    .filter((a) => !a.breaking)
    .slice(0, limit);
}
